%此代码为四题的评价函数/画图用函数
function p=func_four(x)
sigma1=x(1);%直线阻尼器系数
sigma2=x(2);%旋转阻尼器系数
m1=4866;%浮子质量
m2=2433;%振子质量
%由资料得自由浮体在水中的转轴为过自身的重心所在直线
h=2.2079;%浮子重心距离圆锥顶部的距离,计算方法见论文
dens=187.0506;%密度,求解原理见论文
j1=11575.083;%浮子转动惯量
j2=0;%振子转动惯量(在代码中求得)
rho=1025;%海水密度
g=9.8;%小g
k1=80000;%弹簧系数
k2=250000;%扭转弹簧系数
m3=1091.099;%垂荡附加质量
j3=7142.493;%纵摇附加转动惯量
freq=1.9806;%频率
T=1/freq;%周期
s=pi;%圆柱底面积
zeta1=528.5018;%垂荡兴波阻尼系数
zeta2=1655.909;%纵摇兴波阻尼系数
f=1760;%垂荡激励力振幅
l=2140;%纵摇激励力矩振幅
deltat=0.0001;%最小模拟时间间隔
v1=0;v2=0;%浮子/振子垂荡速度（x1以铅直向上方向为正）
%x2以沿杆方向向上为正
a1=0;a2=0;%浮子/振子垂荡加速度（方向同上）
x1=0;x2=0;%浮子/振子离各自初始点相对位移
%其中，x1为垂荡运动，只考虑竖直方向；
%x2为沿杆运动的位移，其等于弹簧在运动过程中的伸长量（负值即为压缩量）
beta1=0;beta2=0;%浮子/振子角加速度（以波浪推力正值方向为正）
w1=0;w2=0;%浮子/振子角速度（以波浪推力方向为正）
w=0;
theta1=0;theta2=0;%浮子/振子离各自初始点相对角度（以波浪推力方向为正）
t=0;%当前时间
x_spr_ori=0.20196;%弹簧初始状态长度
x_h=0.2;%转轴架顶部与转轴距离
eta=8890.7;%静水恢复力矩参数
for i=1:1000000
    t=t+deltat;
    r2=x_spr_ori+x_h+x2+0.25;
    j2=1/12*m2+m2*r2^2;
    f_spr=m2*g*(1-cos(theta2))+k1*(x1*cos(theta2)-x2);
    a1=(f*cos(freq*t)-f_spr*cos(theta2)-zeta1*v1-sigma1*(v1*cos(theta2)-v2)*cos(theta2)-rho*g*x1*s)/(m1+m3);%计算原理见论文
    a2=(sigma1*(v1*cos(theta2)-v2)+f_spr)/m2;
    beta1=(l*cos(t*freq)-sigma2*(w1-w2)-eta*theta1-k2*(theta1-theta2)-zeta2*w1)/(j1+j3);%计算原理见论文
    beta2=(k2*(theta1-theta2)+sigma2*(w1-w2))/j2;
    x1_t=x1;x2_t=x2;%临时存放
    theta1_t=theta1;theta2_t=theta2;%临时存放
    x1=x1+v1*deltat;
    x2=x2+v2*deltat;
    theta1=theta1+w1*deltat;
    theta2=theta2+w2*deltat;
    w=w+abs((x1_t*cos(theta2_t)-x2_t-x1*cos(theta2)+x2)*sigma1*(v1*cos(theta2)-v2));%沿杆方向功计算
    w=w+abs((theta1-theta2-theta1_t+theta2_t)*sigma2*(w1-w2));%转动做功计算
    v1=v1+a1*deltat;
    v2=v2+a2*deltat;
    w1=w1+beta1*deltat;
    w2=w2+beta2*deltat;
end
p=-w/100;

